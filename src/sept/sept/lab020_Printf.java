package sept.sept;

public class lab020_Printf {
    public static void main(String[] args) {
        int a=2;
        System.out.printf("%d",a);

        System.out.println(" ");

        Float f=3.1f;
        System.out.printf("%f",f);
        System.out.println(" ");

        char c='A';
        String s ="Kavita jha";
        System.out.printf("%c",c);
        System.out.println(" ");
        System.out.printf("%s",s);
    }
}
