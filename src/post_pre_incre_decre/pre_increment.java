package post_pre_incre_decre;

public class pre_increment {
    public static void main(String[] args) {

        //Pre Increment
        int a=10;
        System.out.println(a);

        int b=++a;
        System.out.println(b);

        int c=a+b;
        System.out.println(c);

        int a1=15;
        System.out.println(++a1);

    }
}
