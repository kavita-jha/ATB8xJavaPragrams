package operators;

public class compound_operator {
    public static void main(String[] args) {
        // If we want to add some value to the variable which is already assigned then use +,-,*./

        int a=10;
         a+=5;
        System.out.println(a);

        float f= 23.54f;
        f-=5.45f;
        System.out.println(f);

        double d=345.4;
        d*=3;
        System.out.println(d);

        long l=123345l;
        l/=10l;
        System.out.println(l);
    }
}
